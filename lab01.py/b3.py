a = int(input("Nhập số nguyên dương a: "))
b = int(input("Nhập số nguyên dương b: "))


phan_nguyen = a // b
phan_du = a % b

print("Phần nguyên của phép chia a cho b là:", phan_nguyen)
print("Phần dư của phép chia a cho b là:", phan_du)