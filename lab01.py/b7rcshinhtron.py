import math
#Tính chu vi và diện tích hình tròn"

r = float(input("Nhập bán kính hình tròn:"))

chu_vi = 2*math.pi*r
print ("Chu vi hình tròn là: ", chu_vi)

dien_tich = math.pi*r**2
print ("Diện tích hình tròn là: ", dien_tich)
